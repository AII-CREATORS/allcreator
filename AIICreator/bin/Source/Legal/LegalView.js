	_bindEvents()
	{
		var el = this.getElement()

		el.querySelectorAll('.legal-tab').forEach(function(tab)
		{
			tab.addEventListener('click', function(e)
			{
				e.preventDefault()
				el.querySelectorAll('.legal-tab').forEach(function(t) { t.classList.remove('active') })
				tab.classList.add('active')

				var target = el.querySelector('#' + tab.getAttribute('data-target'))
				if (target) target.scrollIntoView({ behavior: 'smooth', block: 'start' })
			})
		})
	}
